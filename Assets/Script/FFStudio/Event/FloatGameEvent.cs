using UnityEngine;

namespace FFStudio
{
    [ CreateAssetMenu( fileName = "event_", menuName = "FF/Event/FloatGameEvent" ) ]
    public class FloatGameEvent : GameEvent
    {
        public float eventValue;

        public void Raise( float value )
        {
			eventValue = value;
			Raise();
		}
    }
}